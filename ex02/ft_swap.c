#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int tempA = *a;
	int tempB = *b;

	*a = tempB;
	*b = tempA;
	write (1, &a, 1);
}

int main ()
{
	*a = 8;
	*b = 6;
	ft_swap(&a, &b);
	return (0);
}
